void gn28l95_rx_vapd_calibration(void)
{
	uint8 regAddr, regData, regA2T2B3, regA2T2B4;
	uint16 apdDac = 0, apdDacTest, rssiADC;

	const uint32 c_tc = 33, c_ru = 220;
	const float c_rd = 3.9, c_sh = 0.03, c_sl = 0.03;
	const uint32 c_cbr_vop = 3;
	uint32 v_op_apd_dac, v_op_apd_dac_25;
	float v_br, v_apd;
	int32 temperature;

	double temperature_get;
	rtk_transceiver_data_t dataCfg, readableCfg;

	rtkbosa_byte_write(TRANSCEIVER_A2, 127, 2);

	/* Disable Write proptect */
	rtkbosa_byte_write(TRANSCEIVER_A2, 0xc2, 0x0);

	/* Disable APD_DAC_LUT */
	rtkbosa_byte_write(TRANSCEIVER_A2, 0xad, 0x8a);

	/* APD Reset */
	rtkbosa_byte_read(TRANSCEIVER_A2, 0x7d, &regData);
	if (regData != 0)	rtkbosa_byte_write(TRANSCEIVER_A2, 0x7d, 0x05);

	rtkbosa_byte_read(TRANSCEIVER_A2, 0xae, &regData);
	RTKBOSA_MSG("A2.0xae (APD_CTRL_2) : 0x%02x", regData);
	apdDacTest = 0;

	while (1) {
		regA2T2B3 = ((apdDacTest << 7) >> 8);
		regA2T2B4 = ((apdDacTest << 7) & 0x80);
		rtkbosa_byte_write(TRANSCEIVER_A2, 0xb3, regA2T2B3);
		rtkbosa_byte_write(TRANSCEIVER_A2, 0xb4, regA2T2B4);
		msleep(150);

		rssiADC = 0;
		rtkbosa_byte_read(TRANSCEIVER_A2, 0xf0, &regData);
		rssiADC |= (regData << 8);
		rtkbosa_byte_read(TRANSCEIVER_A2, 0xf1, &regData);
		rssiADC |= (regData);

		rtkbosa_byte_write(TRANSCEIVER_A2, 0xb3, 0);
		rtkbosa_byte_write(TRANSCEIVER_A2, 0xb4, 0);

		RTKBOSA_MSG("%sTest apdDac = 0x%04x (B3/0x%02x;B4/0x%02x) - rssiADC = 0x%04x %s",
			(rssiADC > 0x0140 ? "\033[1;33;41m" : ""),
			apdDacTest, regA2T2B3, regA2T2B4, rssiADC,
			(rssiADC > 0x0140 ? "(OVER!!!)\033[m" : ""));

		if (rssiADC > 0x0140)	break;
		else apdDac = apdDacTest;

		apdDacTest++;
	}

	//apdDac = 261;	/* sample case for checking, do not use in normal case */
	regA2T2B3 = ((apdDac << 7) >> 8);
	regA2T2B4 = ((apdDac << 7) & 0x80);

	v_op_apd_dac =(uint32)(apdDac - ((c_cbr_vop * 511 / 1.261) * (c_rd / (c_rd + c_ru))));
	v_op_apd_dac_25 = (uint32)(v_op_apd_dac - c_sh * ((c_tc - 25) / ((1.261 / 511) * (1 + (c_ru / c_rd)))));

	v_br = apdDac * 1.2611 / 511 * (1 + c_ru / c_rd);
	v_apd = v_op_apd_dac * 1.2611 / 511 * (1 + c_ru / c_rd);

	rtk_ponmac_transceiver_get(RTK_TRANSCEIVER_PARA_TYPE_TEMPERATURE, &dataCfg);
	_get_data_by_type(RTK_TRANSCEIVER_PARA_TYPE_TEMPERATURE, &dataCfg, &readableCfg);
	temperature_get = atof(readableCfg.buf);

	RTKBOSA_MSG("===============================================");
	RTKBOSA_MSG("Tc :\t\t\t%d", c_tc);
	RTKBOSA_MSG("Ru (K) :\t\t%d", c_ru);
	RTKBOSA_MSG("Rd (K) :\t\t%f", c_rd);
	RTKBOSA_MSG("S_H :\t\t\t%f", c_sh);
	RTKBOSA_MSG("S_L :\t\t\t%f", c_sl);
	RTKBOSA_MSG("A2.T2.0xB3 :\t\t0x%02x", regA2T2B3);
	RTKBOSA_MSG("A2.T2.0xB4 :\t\t0x%02x", regA2T2B4);
	RTKBOSA_MSG("Vbr APD_DAC :\t\t""\033[1;33;46m""%d (0x%04x)""\033[m", apdDac, apdDac);
	RTKBOSA_MSG("Vop APD_DAC :\t\t%d", v_op_apd_dac);
	RTKBOSA_MSG("Vop APD_DAC (25 C) :\t%d ", v_op_apd_dac_25);
	RTKBOSA_MSG("Vbr :\t\t\t%f", v_br);
	RTKBOSA_MSG("Vapd :\t\t\t%f", v_apd);
	RTKBOSA_MSG("Temperature :\t\t%s (%lf)", readableCfg.buf ,temperature_get);
	RTKBOSA_MSG("===============================================");

	rtkbosa_byte_write(TRANSCEIVER_A2, 127, 6);
	for (temperature = -40 , regAddr = 0xC0 ; ((temperature <= 115) && (regAddr <= 0xFF)) ; temperature += 5 , regAddr += 2) {
		apdDac = (uint32)(v_op_apd_dac_25 + c_sl * ((temperature - 25) / ((1.261 / 511) * (1 + (c_ru / c_rd)))));

		RTKBOSA_MSG("%3d C: apdDac = %3d [0x%02X = 0x%02x] [0x%02X = 0x%02x]", temperature, apdDac, regAddr, (apdDac >> 1), regAddr + 1, ((apdDac << 0x7) & 0x80));
		rtkbosa_byte_write(TRANSCEIVER_A2, regAddr, (apdDac >> 1));
		rtkbosa_byte_write(TRANSCEIVER_A2, regAddr + 1, ((apdDac << 0x7) & 0x80));
	}
	bosa_reg_data_dump(TRANSCEIVER_A2);

	rtkbosa_byte_write(TRANSCEIVER_A2, 127, 2);
	rtkbosa_byte_write(TRANSCEIVER_A2, 0xad, 0xca);
	rtkbosa_byte_write(TRANSCEIVER_A2, 0xc2, 0x1);

	return;
}

