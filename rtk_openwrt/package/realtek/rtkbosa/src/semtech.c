#define GN28L96_BEFOREK_CHECK_COUNT	(3)
ld_data_filed_t	gn28l96_beforeK_data[GN28L96_BEFOREK_CHECK_COUNT] = {
	{ TRANSCEIVER_A2, 0x80, 0x8F, 0x9F, "TX_POWER"},
	{ TRANSCEIVER_A2, 0x81, 0xD0, 0x11, "TX_PWD_DDMI"},
	{ TRANSCEIVER_A2, 0x81, 0xD1, 0x11, "TX_PWD_DDMI"},
};

int gn28l96_is_calibrated()
{
	uint32 i, rv = 1;
	uint8 regData;

	for (i = 0 ; i < GN28L96_BEFOREK_CHECK_COUNT ; i++) {
		if (rtkbosa_byte_write(TRANSCEIVER_A2, 127, gn28l96_beforeK_data[i].table) != 0) {
			RTKBOSA_ERROR("rtkbosa_byte_write FAIL !!!");
			return -1;
		}
		if (rtkbosa_byte_read(gn28l96_beforeK_data[i].address, gn28l96_beforeK_data[i].byte, &regData) != 0) {
			RTKBOSA_ERROR("rtkbosa_byte_read FAIL !!!");
			return -1;
		}
		RTKBOSA_MSG("%02X.Table 0x%02x [0x%02x]: 0x%02x (%s)",
			(gn28l96_beforeK_data[i].address << 1), gn28l96_beforeK_data[i].table, gn28l96_beforeK_data[i].byte, regData, gn28l96_beforeK_data[i].description);
		rv &= (regData == gn28l96_beforeK_data[i].value);
	}

	if (rv)
		RTKBOSA_ERROR("Bosa is NOT Calibrated");
	else
		RTKBOSA_INFO("Bosa is Calibrated");

	return 0;
}

