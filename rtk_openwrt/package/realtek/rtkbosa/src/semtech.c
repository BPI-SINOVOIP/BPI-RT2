#define DDMI_TEMPERATURE_DEVIATION_ACCEPT 	(0.3000)
#define DDMI_TEMPERATURE_DEVIATION_LARGE 	(0.5000)

void gn28l95_ddmi_k_temperature(double temperature_measure)
{
	double temperature_get, temperature_delta;
	uint8 regData;
	rtk_transceiver_data_t dataCfg, readableCfg;

	printf("\n[%s]: %f C (INPUT)\n", __FUNCTION__, temperature_measure);
	rtkbosa_byte_write(TRANSCEIVER_A2, 127, 2);

	printf("Set A2.T2.0xD5 as 0x10\n");
	rtkbosa_byte_write(TRANSCEIVER_A2, 0xd5, 0x10);

	while (1) {
		rtk_ponmac_transceiver_get(RTK_TRANSCEIVER_PARA_TYPE_TEMPERATURE, &dataCfg);
		_get_data_by_type(RTK_TRANSCEIVER_PARA_TYPE_TEMPERATURE, &dataCfg, &readableCfg);
		temperature_get = atof(readableCfg.buf);

		temperature_delta = temperature_measure - temperature_get;

		if (temperature_delta >= DDMI_TEMPERATURE_DEVIATION_LARGE) {
			printf("pon get transceiver temperature: %s (---> tuning UP)\n", readableCfg.buf);
			rtkbosa_byte_read(TRANSCEIVER_A2, 0xd4, &regData);
			rtkbosa_byte_write(TRANSCEIVER_A2, 0xd4, regData + 1);
		}
		else if ((DDMI_TEMPERATURE_DEVIATION_ACCEPT < temperature_delta) && (temperature_delta < DDMI_TEMPERATURE_DEVIATION_LARGE)) {
			printf("pon get transceiver temperature: %s (---> tuning up)\n", readableCfg.buf);
			rtkbosa_byte_read(TRANSCEIVER_A2, 0xd5, &regData);
			if (regData <= 0xD0)
				rtkbosa_byte_write(TRANSCEIVER_A2, 0xd5, regData + 0x20);
		}
		else if ((-DDMI_TEMPERATURE_DEVIATION_ACCEPT > temperature_delta) && (temperature_delta > -DDMI_TEMPERATURE_DEVIATION_LARGE)) {
			printf("pon get transceiver temperature: %s (---> tuning down)\n", readableCfg.buf);
			rtkbosa_byte_read(TRANSCEIVER_A2, 0xd5, &regData);
			if (regData >= 0x30)
				rtkbosa_byte_write(TRANSCEIVER_A2, 0xd5, regData - 0x20);
		}
		else if (temperature_delta <= -DDMI_TEMPERATURE_DEVIATION_LARGE) {
			printf("pon get transceiver temperature: %s (---> tuning DOWN)\n", readableCfg.buf);
			rtkbosa_byte_read(TRANSCEIVER_A2, 0xd4, &regData);
			rtkbosa_byte_write(TRANSCEIVER_A2, 0xd4, regData - 1);
		}
		else {
			/* -DDMI_TEMPERATURE_DEVIATION_ACCEPT >= temperature_delta <= DDMI_TEMPERATURE_DEVIATION_ACCEPT */
			break;
		}
		sleep(1);
	}

	printf("pon get transceiver temperature: %s (FINAL)\n", readableCfg.buf);
	rtkbosa_byte_read(TRANSCEIVER_A2, 0xd4, &regData);
	RTKBOSA_MSG("A2.0xd4: 0x%02x", regData);
	rtkbosa_byte_read(TRANSCEIVER_A2, 0xd5, &regData);
	RTKBOSA_MSG("A2.0xd5: 0x%02x", regData);

	return;
}

