INT8U TEST_SHAGenerate(INT32U hashsize, INT8U *expected_digest)
{
	INT8U  digest[SHA512_MAC_LEN];
	INT8U *addr[1];
	INT32U len[1];
	char   err[256];
	INT8U  ret = 0;

	addr[0] = cleartext;
	len[0]  = sizeof(cleartext);

	switch (hashsize) {
	case SHA256_MAC_LEN:
		ret = PLATFORM_SHA256(ARRAY_SIZE(addr), addr, len, digest);
		break;
	case SHA384_MAC_LEN:
		ret = PLATFORM_SHA384(ARRAY_SIZE(addr), addr, len, digest);
		break;
	case SHA512_MAC_LEN:
		ret = PLATFORM_SHA512(ARRAY_SIZE(addr), addr, len, digest);
		break;
	default:
		PLATFORM_SNPRINTF(err, sizeof(err), "hashsize error!");
		goto fail;
	}

	if (ret != 1) {
		PLATFORM_SNPRINTF(err, sizeof(err), "PLATFORM_SHA%d returned false!", hashsize * 8);
		goto fail;
	}

	if (PLATFORM_MEMCMP(digest, expected_digest, hashsize) != 0) {
		PLATFORM_SNPRINTF(err, sizeof(err), "Calculated result is not expected!");
		goto fail;
	}

	PLATFORM_PRINTF("TEST_SHAGenerate               - %-70d: OK\n", hashsize * 8);
	return 0;

fail:
	PLATFORM_PRINTF("TEST_SHAGenerate               - %-70d: KO !!!\n", hashsize * 8);
	PLATFORM_PRINTF("  %s\n", err);
	return 1;
}

