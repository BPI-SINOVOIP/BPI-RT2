INT8U verifyPrivateKey(ECCKeyPair *keypair, const char *curve, char *err, int errlen)
{
	if (keypair->type != PRIVATE_KEY) {
		PLATFORM_SNPRINTF(err, errlen, "Generated key is not PRIVATE_KEY!");
		return 1;
	}

	if (PLATFORM_STRCMP(keypair->curve, curve) != 0) {
		PLATFORM_SNPRINTF(err, errlen, "Curve mismatch (%s vs %s)!", keypair->curve, curve);
		return 1;
	}

	if (keypair->priv_len == 0 || PLATFORM_MEMCMP(keypair->priv, ZERO_ARRAY, MAX_ASN1_DER_PRIVKEY_LEN) == 0) {
		PLATFORM_SNPRINTF(err, errlen, "Private key is empty!");
		return 1;
	}

	if (keypair->priv_len > MAX_ASN1_DER_PRIVKEY_LEN) {
		PLATFORM_SNPRINTF(err, errlen, "Private key length exceeded the limit!");
		return 1;
	}

	if (keypair->pub_ansix963_len == 0 || PLATFORM_MEMCMP(keypair->pub_ansix963, ZERO_ARRAY, MAX_ANSI_X963_LEN) == 0) {
		PLATFORM_SNPRINTF(err, errlen, "ANSI X963 is empty!");
		return 1;
	}

	if (keypair->pub_ansix963_len > MAX_ANSI_X963_LEN) {
		PLATFORM_SNPRINTF(err, errlen, "ANSI X963 length exceeded the limit!");
		return 1;
	}

	if (keypair->subpkinfo_len == 0 || PLATFORM_MEMCMP(keypair->subpkinfo, ZERO_ARRAY, MAX_SUBJECT_PUBKEY_INFO_LEN) == 0) {
		PLATFORM_SNPRINTF(err, errlen, "SubjectPublicKeyInfo is empty!");
		return 1;
	}

	if (keypair->subpkinfo_len > MAX_SUBJECT_PUBKEY_INFO_LEN) {
		PLATFORM_SNPRINTF(err, errlen, "SubjectPublicKeyInfo length exceeded the limit!");
		return 1;
	}

	return 0;
}

INT8U verifyPublicKey(ECCKeyPair *keypair, const char *curve, char *err, int errlen)
{
	if (keypair->type != PUBLIC_KEY) {
		PLATFORM_SNPRINTF(err, errlen, "Generated key is not PUBLIC_KEY!");
		return 1;
	}

	if (PLATFORM_STRCMP(keypair->curve, curve) != 0) {
		PLATFORM_SNPRINTF(err, errlen, "Curve mismatch (%s vs %s)!", keypair->curve, curve);
		return 1;
	}

	if (keypair->priv_len != 0 || PLATFORM_MEMCMP(keypair->priv, ZERO_ARRAY, MAX_ASN1_DER_PRIVKEY_LEN) != 0) {
		PLATFORM_SNPRINTF(err, errlen, "Private key is not empty!");
		return 1;
	}

	if (keypair->pub_ansix963_len == 0 || PLATFORM_MEMCMP(keypair->pub_ansix963, ZERO_ARRAY, MAX_ANSI_X963_LEN) == 0) {
		PLATFORM_SNPRINTF(err, errlen, "ANSI X963 is empty!");
		return 1;
	}

	if (keypair->pub_ansix963_len > MAX_ANSI_X963_LEN) {
		PLATFORM_SNPRINTF(err, errlen, "ANSI X963 length exceeded the limit!");
		return 1;
	}

	if (keypair->subpkinfo_len == 0 || PLATFORM_MEMCMP(keypair->subpkinfo, ZERO_ARRAY, MAX_SUBJECT_PUBKEY_INFO_LEN) == 0) {
		PLATFORM_SNPRINTF(err, errlen, "SubjectPublicKeyInfo is empty!");
		return 1;
	}

	if (keypair->subpkinfo_len > MAX_SUBJECT_PUBKEY_INFO_LEN) {
		PLATFORM_SNPRINTF(err, errlen, "SubjectPublicKeyInfo length exceeded the limit!");
		return 1;
	}

	return 0;
}

