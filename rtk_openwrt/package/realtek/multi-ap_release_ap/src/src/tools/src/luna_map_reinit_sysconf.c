uint8_t _set_ap_mib(uint8_t radio_idx, char *interface_name, struct easymesh_interface_mib interface_mib)
{
	FILE *fp=NULL;
	int ret = 1;
	char tmpbuf[256]={0};
	char config_file_name[256] = {0};
	int val = 0;
	int mode = 0, bss_type = 0, hidessid = 0;
	int encrypt = 0, wsc_auth = 0, wsc_enc = 0, wsc_configured= 0, sha256 = 0, dotIEEE80211W = 0;
	int wpaAuth = 0, wpa2UnicastCipher = 0, unicastCipher = 0, wpaPSKFormat = 0;

	if (radio_idx > 2) {
		// Only support wlan0, wlan1 and wlan2
		printf("Unknown radio index %d\n", radio_idx);
		return 1;
	}

	if ((interface_mib.network_type != MULTI_AP_FRONTHAUL_BSS_BIT) && (interface_mib.network_type != MULTI_AP_BACKHAUL_BSS_BIT)) {
		printf("[%s %d] Not multi-ap bss, don't synchronize!\n", __FUNCTION__,__LINE__);
		return 1;
	}

	strcpy(config_file_name, MULTI_AP_TMP_CONFIG_FILE);
	strcat(config_file_name, "-");
	strcat(config_file_name, interface_name);
	fp = fopen(config_file_name, "w");
	if(fp == NULL) {
		printf("[ERROR] Can not open file %s.\n", config_file_name);
		return 0;
	}

	printf("[%s]Applying WSC configuration (%s), filename = %s: \n", __FUNCTION__, interface_name, config_file_name);
	printf("  - SSID            : %s\n", interface_mib.ssid);
	printf("  - NETWORK_KEY     : %s\n", interface_mib.network_key);
	printf("  - BSS_TYPE        : %02x\n", interface_mib.network_type);

	// tear down
	if (0 == interface_mib.is_enabled) {
		val = 1;
		snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_DISABLED=%d\n", val);
		fputs(tmpbuf, fp);
		printf("[WARNING] Disable radio [%s] with tear_down!\n", interface_name);
		//ret = 0; //check by sysconf
		goto end;
	}

	// interface enable
	val = 0;
	snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_DISABLED=%d\n", val);
	printf("Enable Interface %s\n",interface_name);
	fputs(tmpbuf, fp);

	// Set SSID
	snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_SSID=\"%s\"\n", interface_mib.ssid);
	fputs(tmpbuf, fp);

	// Set PSK
	snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_WPA_PSK=\"%s\"\n", interface_mib.network_key);
	fputs(tmpbuf, fp);

	// Set WLAN MODE
	mode = AP_MODE;
	snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_MODE=%d\n", mode);
	fputs(tmpbuf, fp);

	// Set BSS type
	bss_type = (int)interface_mib.network_type;
	snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_MAP_BSS_TYPE=%d\n", bss_type);
	fputs(tmpbuf, fp);

	if (MULTI_AP_BACKHAUL_BSS_BIT == interface_mib.network_type) {
#ifndef RTK_MULTI_AP_WFA
		hidessid = 1;
#endif
		snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_HIDDEN_SSID=%d\n", hidessid);
		fputs(tmpbuf, fp);
	}

#ifdef WLAN_11W
		sha256        = 0;
		dotIEEE80211W = 0;
#endif
#ifdef WLAN_WPA3
	if (interface_mib.authentication_type == (AUTH_WPA2 | AUTH_WPA3)) {
		encrypt       = WIFI_SEC_WPA3 | WIFI_SEC_WPA2;
		wsc_auth      = WSC_AUTH_WPA3SAE | WSC_AUTH_WPA2PSK;
#ifdef WLAN_11W
		sha256        = 0;
		dotIEEE80211W = 1;
#endif
	} else if (interface_mib.authentication_type == AUTH_WPA3) {
		encrypt       = WIFI_SEC_WPA3;
		wsc_auth      = WSC_AUTH_WPA3SAE;
#ifdef WLAN_11W
		sha256        = 1;
		dotIEEE80211W = 2;
#endif
	} else if (interface_mib.authentication_type == (AUTH_WPA | AUTH_WPA2)) {
		encrypt       = WIFI_SEC_WPA2_MIXED;
		wsc_auth      = WSC_AUTH_WPA2PSKMIXED;
	} else {
		encrypt       = WIFI_SEC_WPA2;
		wsc_auth      = WSC_AUTH_WPA2PSK;

#ifdef RTK_MULTI_AP_WFA
		if (interface_mib.network_type & (MULTI_AP_FRONTHAUL_BSS_BIT | MULTI_AP_BACKHAUL_BSS_BIT)) {
#ifdef WLAN_11W
			dotIEEE80211W = 1;
#endif
		}
#endif
	}
#else
	encrypt       = ENCRYPT_WPA2;
	wsc_auth      = WSC_AUTH_WPA2PSK;

#ifdef RTK_MULTI_AP_WFA
	if (interface_mib.network_type & (MULTI_AP_FRONTHAUL_BSS_BIT | MULTI_AP_BACKHAUL_BSS_BIT)) {
#ifdef WLAN_11W
		dotIEEE80211W = 1;
#endif
	}
#endif
#endif //WLAN_WPA3
	wpa2UnicastCipher = WPA_CIPHER_AES;
	unicastCipher     = 0;
	wsc_enc = WSC_ENCRYPT_AES;
	if (!(interface_mib.network_type & MULTI_AP_BACKHAUL_BSS_BIT)) {
		if (interface_mib.authentication_type == (AUTH_WPA | AUTH_WPA2)) {
			encrypt           = WIFI_SEC_WPA2_MIXED;
			wsc_auth          = WSC_AUTH_WPA2PSKMIXED;
			wpa2UnicastCipher = WPA_CIPHER_MIXED;
			unicastCipher     = WPA_CIPHER_MIXED;
			wsc_enc           = WSC_ENCRYPT_TKIPAES;
		} else if (interface_mib.authentication_type == AUTH_WPA) {
			encrypt           = ENCRYPT_WPA;
			wsc_auth          = WSC_AUTH_WPAPSK;
			wsc_enc           = WSC_ENCRYPT_TKIP;
		} else if (interface_mib.authentication_type == AUTH_DISABLED){
			encrypt           = ENCRYPT_DISABLED;
			wsc_auth          = WSC_AUTH_OPEN;
			wsc_enc           = WSC_ENCRYPT_NONE;
		}
	}

#ifdef MULTI_AP_OPEN_ENCRYPT_SUPPORT
if (interface_mib.authentication_type == 1){
	encrypt       = ENCRYPT_DISABLED;
	wsc_auth      = WSC_AUTH_OPEN;
}
#endif
	wpaAuth           = WPA_AUTH_PSK;
#ifdef WLAN_WPA3
	if(encrypt & WIFI_SEC_WPA3)
	{
		wpa2UnicastCipher = WPA_CIPHER_AES;
		unicastCipher     = 0;
	}
	else
	{
		wpa2UnicastCipher = WPA_CIPHER_AES;
		unicastCipher     = WPA_CIPHER_AES;
	}
#else
	wpa2UnicastCipher = WPA_CIPHER_AES;
	unicastCipher     = WPA_CIPHER_AES;
#endif
	wpaPSKFormat      = 0; //PSK_FORMAT_PASSPHRASE

	// Set Encrypt
	snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_ENCRYPT=%d\n", encrypt);
	fputs(tmpbuf, fp);

	// Set Auth
	snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_WPA_AUTH=%d\n", wpaAuth);
	fputs(tmpbuf, fp);

	// Set WPA2 Cipher suite
	snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_WPA2_CIPHER_SUITE=%d\n", wpa2UnicastCipher);
	fputs(tmpbuf, fp);

	// Set WPA Cipher suite
	snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_WPA_CIPHER_SUITE=%d\n", unicastCipher);
	fputs(tmpbuf, fp);

	// Set PSK_FORMAT_PASSPHRASE
	snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_PSK_FORMAT=%d\n", wpaPSKFormat);
	fputs(tmpbuf, fp);

	// Set dotIEEE80211W
	snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_DOTIEEE80211W=%d\n", dotIEEE80211W);
	fputs(tmpbuf, fp);

	// Set sha256
	snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_SHA256=%d\n", sha256);
	fputs(tmpbuf, fp);

	// Set WSC Auth
	snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_WSC_AUTH=%d\n", wsc_auth);
	fputs(tmpbuf, fp);

	// Set WSC Encrypt
	snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_WSC_ENC=%d\n", wsc_enc);
	fputs(tmpbuf, fp);

	// Set WSC PSK
	snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_WSC_PSK=\"%s\"\n", interface_mib.network_key);
	fputs(tmpbuf, fp);

	// Set WSC Configured
	wsc_configured = 1;
	snprintf(tmpbuf, sizeof(tmpbuf), "WLAN_WSC_CONFIGURED=\"%d\"\n", wsc_configured);
	fputs(tmpbuf, fp);

end:
	if(fp) fclose(fp);
	if(ret == 1)
	{
		_rtk_syscall_map_configure(interface_name, config_file_name);
	}

	return ret;
}

