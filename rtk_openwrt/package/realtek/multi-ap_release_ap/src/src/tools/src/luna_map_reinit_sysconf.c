#define RTL8192CD_IOCTL_GET_MIB 0x89f2
uint8_t _rtk_get_mib(char *interfacename, char *mibname, void *result, int size)
{
	int          skfd;
	struct iwreq wrq;
	struct ifreq rq;
	char         tmp[100];

	skfd = socket(AF_INET, SOCK_DGRAM, 0);
	if (skfd < 0) {
		printf("ioctl [RTL8192CD_IOCTL_GET_MIB] socket error\n");
		return 0;
	}

	memset(&wrq, 0, sizeof(wrq));
	strcpy(wrq.ifr_name, interfacename);
	strcpy(tmp, mibname);

	wrq.u.data.pointer = tmp;
	wrq.u.data.length  = strlen(tmp);
	memcpy(&rq, &wrq, sizeof(struct iwreq));
	/* Do the request */
	if (ioctl(skfd, RTL8192CD_IOCTL_GET_MIB, &rq) < 0) {
		printf("ioctl [RTL8192CD_IOCTL_GET_MIB] for mib %s on %s failed!\n", mibname, interfacename);
		close(skfd);
		return 0;
	}

	if (size)
		memcpy(result, tmp, size);
	else
		strcpy(result, (char *)tmp);

	close(skfd);
	return 0;
}

uint8_t _is_interface_up(char *interface)
{
	int          skfd = 0;
	struct ifreq ifr;

	skfd = socket(AF_INET, SOCK_DGRAM, 0);
	if (skfd < 0) {
		printf("Failed to create socket...\n");
		return 0;
	}

	strncpy(ifr.ifr_name, interface, IFNAMSIZ - 1);

	if (ioctl(skfd, SIOCGIFFLAGS, &ifr) < 0) {
		close(skfd);
		return 0;
	}
	close(skfd);
	return (uint8_t) !!(ifr.ifr_flags & IFF_UP);
}

